# Crie um programa que pede para o usuário digitar 
# 3 notas de um aluno. Em seguida o programa deve
# calcular a média e mostrar na tela!

nota_1 = int(input("Digite o valor da nota 1: "))
nota_2 = int(input("Digite o valor da nota 2: "))
nota_3 = int(input("Digite o valor da nota 3: "))

media = (nota_1 + nota_2 + nota_3) / 3

print("A média do alino é : " , media)
